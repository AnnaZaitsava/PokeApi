import UIKit

extension UIFont {
    
    static func bold28() -> UIFont? {
        return UIFont(name: "Poppins-ExtraBold", size: 28)
    }
    
    static func bold32() -> UIFont? {
        return UIFont(name: "Poppins-ExtraBold", size: 32)
    }
    
    static func bold20() -> UIFont? {
        return UIFont(name: "Poppins-ExtraBold", size: 20)
    }
    
    static func bold16() -> UIFont? {
        return UIFont(name: "Poppins-ExtraBold", size: 16)
    }
    
    static func medium18() -> UIFont? {
        return UIFont(name: "Poppins-Medium", size: 18)
    }
}
